package com.xbuilders.engine.common.utils.imageAtlas;

public class ImageAtlasPosition {

    public float minX, minY, maxX, maxY;

    public ImageAtlasPosition(float minX, float minY, float maxX, float maxY){
        this.minX = minX;
        this.maxX = maxX;
        this.minY = minY;
        this.maxY = maxY;
    }
}
